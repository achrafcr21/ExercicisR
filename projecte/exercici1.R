# EXERCICI 1
# llegim dos numeros i mostrem suma, resta, multiplicacio i divisio

a <- as.numeric(readline("Introdueix el primer número: "))
b <- as.numeric(readline("Introdueix el segon número: "))

cat("Suma:", a + b, "\n")
cat("Resta:", a - b, "\n")
cat("Producte:", a * b, "\n")
cat("Divisió:", a / b, "\n\n")
